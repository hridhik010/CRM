class Employee {
  final String id;
  final String name;
  final String email;
  final String position;
  final String department;
  final String address;
  final String phonenumber;

  Employee({
    required this.id,
    required this.name,
    required this.email,
    required this.position,
    required this.department,
    required this.address,
    required this.phonenumber,
  });
}
